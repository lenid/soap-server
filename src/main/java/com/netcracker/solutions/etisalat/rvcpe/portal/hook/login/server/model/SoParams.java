public class SoParams {

    private String login;
    private String host;
    private Integer port;

    public SoParams() {}

    public SoParams(String login, String host, Integer port) {
        this.login = login;
        this.host = host;
        this.port = port;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public Integer getPort() {
        return port;
    }

    public void setPort(Integer port) {
        this.port = port;
    }
}
